#!/usr/bin/env node
/**
 * Generates Docusaurus content from Postman snapshot + OpenAPI.
 * Usage: npm run docusaurus:generate
 */
import { readFileSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { buildOpenApiFromSnapshot } from "./lib/openapi-from-snapshot.mjs";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const SNAPSHOT = join(ROOT, "data", "collection.snapshot.json");
const DOCUSAURUS = join(ROOT, "docusaurus");
const GENERATED = join(DOCUSAURUS, "src", "generated");
const MCP_BLOCK = join(ROOT, "docs", "official", "MCP_INSTALL_BLOCK.html");

function extractBodyHtml(html) {
  const bodyMatch = html.match(/<body[^>]*>([\s\S]*?)<\/body>/i);
  if (bodyMatch) return bodyMatch[1].trim();
  return html
    .replace(/<\/?html[^>]*>/gi, "")
    .replace(/<\/?head[^>]*>[\s\S]*?<\/?head>/gi, "")
    .replace(/<\/?body[^>]*>/gi, "")
    .trim();
}

function resolveOfficialDocsUrl() {
  const url = (process.env.DOCUSAURUS_URL ?? "https://dkanster.github.io").replace(/\/$/, "");
  const base = process.env.DOCUSAURUS_BASE_URL ?? "/samotpravil-mcp/";
  if (base === "/") return `${url}/`;
  return `${url}${base.startsWith("/") ? base : `/${base}`}`;
}

const collection = JSON.parse(readFileSync(SNAPSHOT, "utf8"));
const overviewHtml = extractBodyHtml(collection.info?.description ?? "<p>Документация API СамОтправил.</p>");
const mcpBlock = existsSync(MCP_BLOCK) ? readFileSync(MCP_BLOCK, "utf8") : "";

const { yaml, operationCount, tagCount, overview } = buildOpenApiFromSnapshot(SNAPSHOT, collection, {
  fullDescriptions: true,
  officialDocsUrl: resolveOfficialDocsUrl(),
});

mkdirSync(GENERATED, { recursive: true });
mkdirSync(join(DOCUSAURUS, "static"), { recursive: true });
writeFileSync(join(DOCUSAURUS, "static", "openapi.yaml"), yaml, "utf8");

const combinedHtml = `${overviewHtml}\n${mcpBlock}`;

writeFileSync(
  join(GENERATED, "overview.js"),
  `// Auto-generated by scripts/generate-docusaurus-content.mjs — do not edit
export const overviewHtml = ${JSON.stringify(combinedHtml)};
export const meta = ${JSON.stringify({
    title: overview.title,
    operationCount,
    tagCount,
  })};
`,
  "utf8",
);

writeFileSync(
  join(DOCUSAURUS, "docs", "overview.mdx"),
  `---
id: overview
title: Обзор
slug: /
sidebar_position: 1
---

import OverviewContent from '@site/src/components/OverviewContent';

<OverviewContent />
`,
  "utf8",
);

console.log(`Generated Docusaurus content → ${DOCUSAURUS}`);
console.log(`  ${operationCount} operations, ${tagCount} sections, title: ${overview.title}`);
